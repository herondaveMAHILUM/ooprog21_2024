public class MathTest {
   public static void main(String[] args) {
      System.out.println("SQRT: " + Math.sqrt(67));
      System.out.println("SIN: " + Math.sin(270));
      System.out.println("COS: " + Math.cos(270));
      System.out.println("FLOOR: " + Math.floor(150));
      System.out.println("CIEL: " + Math.ceil(150));
      System.out.println("ROUND: " + Math.round(64));
      System.out.println("MAX: " + Math.max(68, 71));
      System.out.println("MIN: " + Math.min(68, 71));
      System.out.println("RANDOM: " + Math.random());
      
   }
}
