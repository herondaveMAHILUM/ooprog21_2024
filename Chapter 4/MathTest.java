public class MathTest {
   public static void main(String[] args) {
      System.out.println("SQRT: " + Math.sqrt(64));
      System.out.println("SIN: " + Math.sin(64));
      System.out.println("COS: " + Math.cos(64));
      System.out.println("FLOOR: " + Math.floor(64));
      System.out.println("CIEL: " + Math.ceil(64));
      System.out.println("ROUND: " + Math.round(64));
      System.out.println("MAX: " + Math.max(1, 100));
      System.out.println("RANDOM: " + Math.random());
      
   }
}
